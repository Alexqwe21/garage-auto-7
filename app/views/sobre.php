document.addEventListener('DOMContentLoaded', function () {

  const linksMenu = document.querySelectorAll('nav ul li a');
  let paginaAtual = window.location.pathname.split('/').pop(); // pega só a última parte da URL

  // se estiver na raiz, considera 'home'
  if (paginaAtual === '') {
    paginaAtual = 'home';
  }

  linksMenu.forEach(link => {
    const href = link.getAttribute('href');

    // remove classe active de todos
    link.classList.remove('active');

    // marca apenas o link correspondente
    if (href === paginaAtual) {
      link.classList.add('active');
    }
  });

});
</script>




  <script>
    const track = document.querySelector('.marcas-track');

    // Duplica os itens para criar o loop infinito
    track.innerHTML += track.innerHTML;

    let position = 0;
    const speed = 0.3; // quanto menor, mais devagar

    function animate() {
      position -= speed;

      // quando andar metade, volta para o início
      if (Math.abs(position) >= track.scrollWidth / 2) {
        position = 0;
      }

      track.style.transform = `translateX(${position}px)`;
      requestAnimationFrame(animate);
    }

    animate();
  </script>











